import type { Event, Message } from "@trace/gql";
import { asJsonObject } from "@trace/shared";
import type { JsonObject } from "@trace/shared";
import {
  useEntityStore,
  eventScopeKey,
  messageScopeKey,
  removeEventIdByScope,
  upsertEventIdByScope,
} from "../stores/entity.js";
import { useAuthStore } from "../stores/auth.js";
import { generateUUID } from "../utils/uuid.js";

type EntityStoreState = {
  messages: Record<string, Message>;
  eventsByScope: Record<string, Record<string, Event>>;
  _eventIdsByScope: Record<string, string[]>;
  _messageIdsByScope: Record<string, string[]>;
};

const OPTIMISTIC_PREFIX = "optimistic:";

/** Returns true if the event ID was generated by an optimistic insert */
export function isOptimisticEvent(id: string): boolean {
  return id.startsWith(OPTIMISTIC_PREFIX);
}

/** Max age (ms) before a pending entry is considered stale and swept. */
const PENDING_TTL_MS = 30_000;

export interface PendingSessionEntry {
  tempEventId: string;
  clientMutationId: string;
  expectedRealEventId?: string;
  createdAt: number;
}

export interface PendingChatEntry {
  tempMessageId: string;
  tempEventId: string;
  clientMutationId: string;
  expectedRealMessageId?: string;
  createdAt: number;
}

const pendingSessionOptimistic = new Map<string, PendingSessionEntry[]>();
const pendingChatOptimistic = new Map<string, PendingChatEntry[]>();

function sweepStalePending(): void {
  const now = Date.now();
  for (const [key, queue] of pendingSessionOptimistic) {
    const fresh = queue.filter((e) => now - e.createdAt < PENDING_TTL_MS);
    if (fresh.length === 0) pendingSessionOptimistic.delete(key);
    else pendingSessionOptimistic.set(key, fresh);
  }
  for (const [key, queue] of pendingChatOptimistic) {
    const fresh = queue.filter((e) => now - e.createdAt < PENDING_TTL_MS);
    if (fresh.length === 0) pendingChatOptimistic.delete(key);
    else pendingChatOptimistic.set(key, fresh);
  }
}

setInterval(sweepStalePending, PENDING_TTL_MS);

function enqueue<T>(map: Map<string, T[]>, key: string, item: T): void {
  const queue = map.get(key);
  if (queue) {
    queue.push(item);
    return;
  }
  map.set(key, [item]);
}

function dequeue<T>(map: Map<string, T[]>, key: string, predicate: (item: T) => boolean): void {
  const queue = map.get(key);
  if (!queue) return;
  const idx = queue.findIndex(predicate);
  if (idx === -1) return;
  queue.splice(idx, 1);
  if (queue.length === 0) map.delete(key);
}

function takeMatching<T>(
  map: Map<string, T[]>,
  key: string,
  predicate: (item: T) => boolean,
): T | undefined {
  const queue = map.get(key);
  if (!queue) return undefined;
  const idx = queue.findIndex(predicate);
  if (idx === -1) return undefined;
  const [entry] = queue.splice(idx, 1);
  if (queue.length === 0) map.delete(key);
  return entry;
}

function takeAllMatching<T>(
  map: Map<string, T[]>,
  key: string,
  predicate: (item: T) => boolean,
): T[] {
  const queue = map.get(key);
  if (!queue) return [];

  const matched: T[] = [];
  const remaining: T[] = [];
  for (const item of queue) {
    if (predicate(item)) {
      matched.push(item);
    } else {
      remaining.push(item);
    }
  }

  if (matched.length === 0) return [];
  if (remaining.length === 0) {
    map.delete(key);
  } else {
    map.set(key, remaining);
  }
  return matched;
}

function patchPending<T>(
  map: Map<string, T[]>,
  key: string,
  predicate: (item: T) => boolean,
  patch: (item: T) => void,
): void {
  const queue = map.get(key);
  if (!queue) return;
  const entry = queue.find(predicate);
  if (!entry) return;
  patch(entry);
}

function createClientMutationId(): string {
  return `${OPTIMISTIC_PREFIX}${generateUUID()}`;
}

function getClientMutationId(event: Event): string | null {
  const payload = asJsonObject(event.payload);
  return typeof payload?.clientMutationId === "string" ? payload.clientMutationId : null;
}

function takePendingSessionEntry(sessionId: string, event: Event): PendingSessionEntry | undefined {
  const clientMutationId = getClientMutationId(event);
  return takeMatching(
    pendingSessionOptimistic,
    sessionId,
    (entry) =>
      (clientMutationId !== null && entry.clientMutationId === clientMutationId) ||
      entry.expectedRealEventId === event.id,
  );
}

export function takePendingOptimisticSession(
  sessionId: string,
  event: Event,
): PendingSessionEntry | undefined {
  return takePendingSessionEntry(sessionId, event);
}

export function takePendingOptimisticChat(
  chatId: string,
  event: Event,
): PendingChatEntry | undefined {
  const clientMutationId = getClientMutationId(event);
  const payload = asJsonObject(event.payload);
  const realMessageId = typeof payload?.messageId === "string" ? payload.messageId : null;

  return takeMatching(
    pendingChatOptimistic,
    chatId,
    (entry) =>
      (clientMutationId !== null && entry.clientMutationId === clientMutationId) ||
      entry.expectedRealMessageId === realMessageId,
  );
}

function upsertSessionEvents(scopeKey: string, events: Array<Event & { id: string }>): void {
  useEntityStore.setState((state: EntityStoreState) => {
    const bucket = { ...(state.eventsByScope[scopeKey] ?? {}) };
    let eventIdsByScope = state._eventIdsByScope;
    for (const event of events) {
      eventIdsByScope = upsertEventIdByScope(
        eventIdsByScope,
        scopeKey,
        event.id,
        event,
        bucket,
        bucket[event.id],
      );
      bucket[event.id] = event;
    }
    return {
      eventsByScope: { ...state.eventsByScope, [scopeKey]: bucket },
      ...(eventIdsByScope !== state._eventIdsByScope ? { _eventIdsByScope: eventIdsByScope } : {}),
    };
  });
}

export interface OptimisticSessionIds {
  eventId: string;
  clientMutationId: string;
}

export function optimisticallyInsertSessionMessage(
  sessionId: string,
  text: string,
  options?: { imageKeys?: string[]; imagePreviewUrls?: string[] },
): OptimisticSessionIds {
  const user = useAuthStore.getState().user;
  const now = new Date().toISOString();
  const clientMutationId = createClientMutationId();
  const tempId = `${OPTIMISTIC_PREFIX}${generateUUID()}`;
  const scopeKey = eventScopeKey("session", sessionId);

  const event: Event = {
    id: tempId,
    scopeType: "session",
    scopeId: sessionId,
    eventType: "message_sent",
    payload: {
      text,
      clientMutationId,
      ...(options?.imageKeys?.length ? { imageKeys: options.imageKeys } : {}),
      ...(options?.imagePreviewUrls?.length ? { imagePreviewUrls: options.imagePreviewUrls } : {}),
    } as JsonObject,
    actor: {
      type: "user",
      id: user?.id ?? "",
      name: user?.name ?? null,
      avatarUrl: user?.avatarUrl ?? null,
    },
    parentId: null,
    timestamp: now,
    metadata: null,
  };

  useEntityStore.getState().upsertScopedEvent(scopeKey, tempId, event);

  enqueue(pendingSessionOptimistic, sessionId, { tempEventId: tempId, clientMutationId, createdAt: Date.now() });

  return { eventId: tempId, clientMutationId };
}

export function removeOptimisticSessionMessage(sessionId: string, tempEventId: string): void {
  dequeue(pendingSessionOptimistic, sessionId, (entry) => entry.tempEventId === tempEventId);

  const scopeKey = eventScopeKey("session", sessionId);
  useEntityStore.setState((state: EntityStoreState) => {
    const bucket = state.eventsByScope[scopeKey];
    if (!bucket || !bucket[tempEventId]) return state;
    const { [tempEventId]: _, ...rest } = bucket;
    const eventIdsByScope = removeEventIdByScope(state._eventIdsByScope, scopeKey, tempEventId);
    return {
      eventsByScope: { ...state.eventsByScope, [scopeKey]: rest },
      ...(eventIdsByScope !== state._eventIdsByScope ? { _eventIdsByScope: eventIdsByScope } : {}),
    };
  });
}

export function reconcileOptimisticSessionMessage(
  sessionId: string,
  tempEventId: string,
  realEventId: string,
): void {
  patchPending(
    pendingSessionOptimistic,
    sessionId,
    (entry) => entry.tempEventId === tempEventId,
    (entry) => {
      entry.expectedRealEventId = realEventId;
    },
  );
}

export function upsertSessionEventWithOptimisticResolution(
  sessionId: string,
  event: Event & { id: string },
): void {
  const scopeKey = eventScopeKey("session", sessionId);
  const pending = takePendingSessionEntry(sessionId, event);
  if (!pending) {
    useEntityStore.getState().upsertScopedEvent(scopeKey, event.id, event);
    return;
  }

  useEntityStore.setState((state: EntityStoreState) => {
    const bucket = { ...(state.eventsByScope[scopeKey] ?? {}) };
    let eventIdsByScope = removeEventIdByScope(state._eventIdsByScope, scopeKey, pending.tempEventId);
    eventIdsByScope = upsertEventIdByScope(
      eventIdsByScope,
      scopeKey,
      event.id,
      event,
      bucket,
      bucket[event.id],
    );
    delete bucket[pending.tempEventId];
    bucket[event.id] = event;
    return {
      eventsByScope: { ...state.eventsByScope, [scopeKey]: bucket },
      ...(eventIdsByScope !== state._eventIdsByScope ? { _eventIdsByScope: eventIdsByScope } : {}),
    };
  });
}

export function upsertFetchedSessionEventsWithOptimisticResolution(
  sessionId: string,
  events: Array<Event & { id: string }>,
): void {
  const scopeKey = eventScopeKey("session", sessionId);
  const eventIds = new Set(events.map((event) => event.id));
  const matched = takeAllMatching(
    pendingSessionOptimistic,
    sessionId,
    (entry) => !!entry.expectedRealEventId && eventIds.has(entry.expectedRealEventId),
  );

  if (matched.length === 0) {
    upsertSessionEvents(scopeKey, events);
    return;
  }

  useEntityStore.setState((state: EntityStoreState) => {
    const bucket = { ...(state.eventsByScope[scopeKey] ?? {}) };
    let eventIdsByScope = state._eventIdsByScope;
    for (const event of events) {
      eventIdsByScope = upsertEventIdByScope(
        eventIdsByScope,
        scopeKey,
        event.id,
        event,
        bucket,
        bucket[event.id],
      );
      bucket[event.id] = event;
    }
    for (const entry of matched) {
      delete bucket[entry.tempEventId];
      eventIdsByScope = removeEventIdByScope(eventIdsByScope, scopeKey, entry.tempEventId);
    }
    return {
      eventsByScope: { ...state.eventsByScope, [scopeKey]: bucket },
      ...(eventIdsByScope !== state._eventIdsByScope ? { _eventIdsByScope: eventIdsByScope } : {}),
    };
  });
}

export interface OptimisticChatIds {
  messageId: string;
  eventId: string;
  clientMutationId: string;
}

export function optimisticallyInsertChatMessage(
  chatId: string,
  html: string,
  parentId?: string | null,
): OptimisticChatIds {
  const user = useAuthStore.getState().user;
  const now = new Date().toISOString();
  const clientMutationId = createClientMutationId();
  const tempEventId = `${OPTIMISTIC_PREFIX}${generateUUID()}`;
  const tempMessageId = `${OPTIMISTIC_PREFIX}${generateUUID()}`;
  const scopeKey = eventScopeKey("chat", chatId);

  const actor = {
    type: "user" as const,
    id: user?.id ?? "",
    name: user?.name ?? null,
    avatarUrl: user?.avatarUrl ?? null,
  };

  useEntityStore.getState().upsert("messages", tempMessageId, {
    id: tempMessageId,
    chatId,
    channelId: null,
    text: html,
    html,
    mentions: null,
    parentMessageId: parentId ?? null,
    replyCount: 0,
    latestReplyAt: null,
    threadRepliers: [],
    actor,
    createdAt: now,
    updatedAt: now,
    editedAt: null,
    deletedAt: null,
  });

  const event: Event = {
    id: tempEventId,
    scopeType: "chat",
    scopeId: chatId,
    eventType: "message_sent",
    payload: {
      messageId: tempMessageId,
      text: html,
      html,
      parentMessageId: parentId ?? null,
      clientMutationId,
    } as JsonObject,
    actor,
    parentId: null,
    timestamp: now,
    metadata: null,
  };

  useEntityStore.getState().upsertScopedEvent(scopeKey, tempEventId, event);

  enqueue(pendingChatOptimistic, chatId, {
    tempMessageId,
    tempEventId,
    clientMutationId,
    createdAt: Date.now(),
  });

  return { messageId: tempMessageId, eventId: tempEventId, clientMutationId };
}

export function removeOptimisticChatMessage(
  chatId: string,
  tempMessageId: string,
  tempEventId: string,
): void {
  dequeue(
    pendingChatOptimistic,
    chatId,
    (entry) => entry.tempMessageId === tempMessageId && entry.tempEventId === tempEventId,
  );

  const eventSK = eventScopeKey("chat", chatId);
  const msgSK = messageScopeKey("chat", chatId);
  useEntityStore.setState((state: EntityStoreState) => {
    const { [tempMessageId]: _removed, ...restMessages } = state.messages;

    let nextMsgIndex = state._messageIdsByScope;
    const scopeIds = nextMsgIndex[msgSK];
    if (scopeIds?.includes(tempMessageId)) {
      const filtered = scopeIds.filter((id: string) => id !== tempMessageId);
      nextMsgIndex = { ...nextMsgIndex, [msgSK]: filtered };
    }

    let nextEventsByScope = state.eventsByScope;
    let nextEventIdsByScope = state._eventIdsByScope;
    const bucket = nextEventsByScope[eventSK];
    if (bucket?.[tempEventId]) {
      const { [tempEventId]: _evt, ...rest } = bucket;
      nextEventsByScope = { ...nextEventsByScope, [eventSK]: rest };
      nextEventIdsByScope = removeEventIdByScope(nextEventIdsByScope, eventSK, tempEventId);
    }

    return {
      messages: restMessages,
      _messageIdsByScope: nextMsgIndex,
      eventsByScope: nextEventsByScope,
      _eventIdsByScope: nextEventIdsByScope,
    };
  });
}

export function reconcileOptimisticChatMessage(
  chatId: string,
  tempMessageId: string,
  tempEventId: string,
  realMessageId: string,
): void {
  patchPending(
    pendingChatOptimistic,
    chatId,
    (entry) => entry.tempMessageId === tempMessageId && entry.tempEventId === tempEventId,
    (entry) => {
      entry.expectedRealMessageId = realMessageId;
    },
  );
}

export function upsertFetchedChatMessagesWithOptimisticResolution(
  chatId: string,
  messages: Array<Message & { id: string }>,
): void {
  const scopeKey = eventScopeKey("chat", chatId);
  const realMessageIds = new Set(messages.map((message) => message.id));
  const matched = takeAllMatching(
    pendingChatOptimistic,
    chatId,
    (entry) => !!entry.expectedRealMessageId && realMessageIds.has(entry.expectedRealMessageId),
  );

  if (matched.length === 0) {
    useEntityStore.getState().upsertMany("messages", messages);
    return;
  }

  const msgSK = messageScopeKey("chat", chatId);
  useEntityStore.setState((state: EntityStoreState) => {
    const nextMessages = { ...state.messages };
    for (const message of messages) {
      nextMessages[message.id] = message;
    }

    for (const entry of matched) {
      delete nextMessages[entry.tempMessageId];
    }

    const nextMsgIndex = { ...state._messageIdsByScope };
    const scopeIds = [...(nextMsgIndex[msgSK] ?? [])];
    const tempIds = new Set(matched.map((e) => e.tempMessageId));
    const filtered = scopeIds.filter((id: string) => !tempIds.has(id));
    for (const message of messages) {
      if (!filtered.includes(message.id)) {
        filtered.push(message.id);
      }
    }
    nextMsgIndex[msgSK] = filtered;

    let nextEventsByScope = state.eventsByScope;
    let nextEventIdsByScope = state._eventIdsByScope;
    let bucket = state.eventsByScope[scopeKey];
    let bucketChanged = false;

    for (const entry of matched) {
      if (bucket?.[entry.tempEventId]) {
        if (!bucketChanged) {
          nextEventsByScope = { ...state.eventsByScope };
          bucket = { ...(bucket ?? {}) };
          nextEventsByScope[scopeKey] = bucket;
          bucketChanged = true;
        }
        delete bucket[entry.tempEventId];
        nextEventIdsByScope = removeEventIdByScope(nextEventIdsByScope, scopeKey, entry.tempEventId);
      }
    }

    return {
      messages: nextMessages,
      _messageIdsByScope: nextMsgIndex,
      ...(bucketChanged ? { eventsByScope: nextEventsByScope } : {}),
      ...(nextEventIdsByScope !== state._eventIdsByScope
        ? { _eventIdsByScope: nextEventIdsByScope }
        : {}),
    };
  });
}
